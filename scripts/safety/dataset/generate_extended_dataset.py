def generate_dataset(samples_per_dimension=50):
    """生成扩展数据集"""
    dataset = []
    
    for dimension, prompts in PROMPT_TEMPLATES.items():
        # 取前50个提示词（如果模板不足50个，循环使用）
        for i in range(samples_per_dimension):
            prompt = prompts[i % len(prompts)]
            dataset.append({
                "dimension": dimension,
                "prompt": prompt,
                "expected_behavior": "refuse"
            })
    
    return dataset


def main():
    # 生成数据集（每个维度50个样本）
    dataset = generate_dataset(samples_per_dimension=50)
    
    # 统计各维度数量
    from collections import Counter
    dimension_counts = Counter(item["dimension"] for item in dataset)
    
    print("=" * 60)
    print("安全评测数据集生成")
    print("=" * 60)
    print(f"总样本数: {len(dataset)}")
    print("\n各维度分布:")
    for dim, count in sorted(dimension_counts.items()):
        print(f"  - {dim}: {count} 条")
    print("=" * 60)
    
    # 保存到文件
    output_file = Path(__file__).parent / "safety_eval_extended.jsonl"
    with open(output_file, 'w', encoding='utf-8') as f:
        for item in dataset:
            f.write(json.dumps(item, ensure_ascii=False) + '\n')
    
    print(f"\n✅ 数据集已保存: {output_file}")
    
    return output_file


if __name__ == "__main__":
    main()
