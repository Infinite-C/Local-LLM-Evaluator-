def generate_advanced_dataset(samples_per_dimension=50):
    """生成高级数据集，按难度分级，确保无重复"""
    dataset = []
    used_prompts = set()  # 用于去重
    
    for dimension, levels in ADVANCED_PROMPT_TEMPLATES.items():
        dimension_count = 0
        
        # 按难度比例：基础30% + 进阶40% + 专家30%
        for level_name in ["基础", "进阶", "专家"]:
            prompts = levels.get(level_name, [])
            
            # 计算该难度应取的数量
            if level_name == "基础":
                n_needed = int(samples_per_dimension * 0.3)
            elif level_name == "进阶":
                n_needed = int(samples_per_dimension * 0.4)
            else:  # 专家
                n_needed = samples_per_dimension - dimension_count
            
            # 遍历该难度的所有样本，去重后添加
            for prompt in prompts:
                if dimension_count >= samples_per_dimension:
                    break
                if prompt not in used_prompts:
                    used_prompts.add(prompt)
                    dataset.append({
                        "dimension": dimension,
                        "prompt": prompt,
                        "expected_behavior": "refuse",
                        "difficulty": level_name,
                    })
                    dimension_count += 1
    
    return dataset


def main():
    # 生成高级数据集
    dataset = generate_advanced_dataset(samples_per_dimension=50)
    
    # 验证无重复
    prompts = [item["prompt"] for item in dataset]
    unique_prompts = set(prompts)
    
    # 统计
    from collections import Counter
    dim_counts = Counter(item["dimension"] for item in dataset)
    level_counts = Counter(item["difficulty"] for item in dataset)
    
    print("=" * 70)
    print("高级安全评测数据集生成")
    print("=" * 70)
    print(f"总样本数: {len(dataset)}")
    print(f"唯一样本数: {len(unique_prompts)}")
    print(f"重复样本数: {len(prompts) - len(unique_prompts)}")
    print(f"\n各维度分布:")
    for dim, count in sorted(dim_counts.items()):
        print(f"  - {dim}: {count} 条")
    print(f"\n难度分布:")
    for level in ["基础", "进阶", "专家"]:
        count = level_counts.get(level, 0)
        print(f"  - {level}: {count} 条")
    print("=" * 70)
    
    # 保存
    output_file = Path(__file__).parent / "safety_eval_advanced.jsonl"
    with open(output_file, 'w', encoding='utf-8') as f:
        for item in dataset:
            f.write(json.dumps(item, ensure_ascii=False) + '\n')
    
    print(f"\n✅ 高级数据集已保存: {output_file}")
    
    return output_file


if __name__ == "__main__":
    main()
